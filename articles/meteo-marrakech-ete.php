                <section class="meteo-article-section">
                    <h2 class="meteo-article-subtitle">Températures Moyennes en Été</h2>
                    <p>Durant les mois d'été (juin à septembre), Marrakech connaît des températures particulièrement élevées. Les maximales atteignent régulièrement 38-42°C en journée, tandis que les minimales nocturnes oscillent entre 22-26°C.</p>
                    <p>Le mois de juillet est généralement le plus chaud de l'année, avec des pics pouvant dépasser les 45°C lors des vagues de chaleur. Les nuits restent chaudes, offrant peu de répit thermique.</p>
                </section>

                <section class="meteo-article-section">
                    <h2 class="meteo-article-subtitle">Précipitations et Humidité</h2>
                    <p>L'été à Marrakech se caractérise par une quasi-absence de précipitations. Les mois de juin, juillet et août sont particulièrement secs, avec moins de 5mm de pluie en moyenne.</p>
                    <p>L'humidité relative reste faible, généralement comprise entre 30% et 50%, ce qui rend la chaleur plus supportable malgré les températures élevées.</p>
                </section>

                <section class="meteo-article-section">
                    <h2 class="meteo-article-subtitle">Conseils pour Visiter Marrakech en Été</h2>
                    <p>Si vous prévoyez de visiter Marrakech pendant l'été, voici quelques recommandations essentielles :</p>
                    <ul class="meteo-article-list">
                        <li>Planifiez vos activités extérieures tôt le matin (avant 10h) ou en fin d'après-midi (après 17h)</li>
                        <li>Portez des vêtements légers, de couleur claire et couvrants</li>
                        <li>Hydratez-vous régulièrement et évitez l'exposition prolongée au soleil</li>
                        <li>Privilégiez les lieux climatisés pendant les heures les plus chaudes</li>
                    </ul>
                </section>

                <section class="meteo-article-section">
                    <h2 class="meteo-article-subtitle">Prévisions et Tendances</h2>
                    <p>Les prévisions météorologiques pour Marrakech en été montrent une tendance à la stabilité, avec des journées ensoleillées et chaudes. Les variations de température d'un jour à l'autre restent généralement faibles.</p>
                    <p>Il est recommandé de consulter régulièrement les prévisions à court terme pour planifier au mieux vos activités et déplacements dans la région.</p>
