                        <li>Choisissez un hébergement climatisé ou bien ventilé.</li>
                        <li>Prévoyez des vêtements légers mais couvrants, un chapeau, des lunettes et une crème solaire.</li>
                        <li>Buvez régulièrement et préférez les activités au bord de la mer (planche à voile, plage d’Agadir ou Essaouira) ou les balades en début ou fin de journée.</li>
                            </ul>
                </section>


