                        21 °C la nuit, et moins de 1 mm de pluie, elle offre une météo équilibrée même durant les jours les plus chauds.</p>

                    <ul class="meteo-article-list">
                        <li>Les alizés marins apportent fraîcheur et humidité modérée.</li>
                        <li>Le taux de soleil est excellent toute la journée.</li>
                        <li>Les plages, les balades en bord de mer et les activités en plein air restent tout à fait possibles sans subir un climat extrême.</li>
                        </ul>
