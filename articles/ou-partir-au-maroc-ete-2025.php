                        <li>Le ressenti « feels like » peut dépasser 40 °C avec peu d’humidité.</li>
                        <li>Pour voyager au Maroc en août dans ces villes, il faut planifier ses visites tôt le matin ou en soirée, prévoir des pauses ombragées et bien s’hydrater.</li>
                            </ul>
                </section>



